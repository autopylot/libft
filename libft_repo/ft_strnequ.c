#include "libft.h"

int	ft_strnequ(char const *s1, char const *s2, size_t n)
{
	if (s1 && s2)
	{
		if (n == 0)
			return (1);
		if (*s1 == '\0' && *s2 == '\0')
			return (1);
		return (ft_strncmp(s1, s2, n) != 0 ? 0 : 1);
	}
	else
		return (0);
}
