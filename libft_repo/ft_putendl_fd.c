#include "libft.h"

void	ft_putendl_fd(char const *s, int fd)
{
	char *s_ptr;

	s_ptr = (char*)s;
	if (s)
	{
		while (*s_ptr != '\0')
			write(fd, s_ptr++, 1);
		write(fd, "\n", 1);
	}
}
