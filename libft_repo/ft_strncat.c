#include "libft.h"

char	*ft_strncat(char *s1, char *s2, size_t n)
{
	char *s1_ptr;
	char *s2_ptr;

	s1_ptr = (s1 + ft_strlen(s1));
	s2_ptr = s2;
	while (n > 0 & *s2_ptr != '\0')
	{
		*s1_ptr++ = *s2_ptr++;
		n--;
	}
	*s1_ptr = '\0';
	return (s1);
}
