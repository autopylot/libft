#include "libft.h"

void	ft_putendl(char const *s)
{
	char *s_ptr;

	if (s)
	{
		s_ptr = (char*)s;
		while (*s_ptr != '\0')
		{
			write(1, s_ptr++, 1);
		}
		write(1, "\n", 1);
	}
}
